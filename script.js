    getFilteredGratitudes() {
        let filtered = [...this.gratitudes];
        
        if (this.searchQuery) {
            filtered = filtered.filter(g => 
                g.text.toLowerCase().includes(this.searchQuery.toLowerCase())
            );
        }
        
        if (this.filterRating !== 'all') {
            const minRating = parseInt(this.filterRating);
            filtered = filtered.filter(g => g.rating >= minRating);
        }
        
        if (this.filterDate === 'week') {
            const weekAgo = new Date();
            weekAgo.setDate(weekAgo.getDate() - 7);
            filtered = filtered.filter(g => new Date(g.date) >= weekAgo);
        } else if (this.filterDate === 'month') {
            const monthAgo = new Date();
            monthAgo.setDate(monthAgo.getDate() - 30);
            filtered = filtered.filter(g => new Date(g.date) >= monthAgo);
        }
        
        return filtered.sort((a, b) => {
            if (a.pinned !== b.pinned) return a.pinned ? -1 : 1;
            return new Date(b.timestamp) - new Date(a.timestamp);
        });
    }
    
