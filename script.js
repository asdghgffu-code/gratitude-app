    checkStreak() {
        const today = new Date().toISOString().split('T')[0];
        const hasTodayEntry = this.gratitudes.some(g => g.date === today);
        
        if (hasTodayEntry && this.lastDate !== today) {
            if (this.lastDate) {
                const yesterday = new Date();
                yesterday.setDate(yesterday.getDate() - 1);
                const yesterdayStr = yesterday.toISOString().split('T')[0];
                this.streak = this.lastDate === yesterdayStr ? this.streak + 1 : 1;
            } else {
                this.streak = 1;
            }
            this.lastDate = today;
            this.totalDays++;
        }
    }
    
